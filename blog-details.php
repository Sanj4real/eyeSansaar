                        <h1>Why We Must Play Games in the Classroom!</h1>
                    </div>
                </div>
                <p class="m-0">Sep 17, 2021</p>
            </div>
            <div>
                <p>Games, you said? It sounds slightly bizarre, right? Perhaps you believe there is a time and place for
                    games, and it is not inside a classroom. Maybe your school has a designated ‘period’ for games like
                    we have for other subjects. Where do games fit inside a classroom? Could it really be an effective
                    learning strategy?&nbsp;</p>

                <p>We all know how important games are. So much so that it is amusing how little they’re used inside a
                    classroom. Games boost reasoning, participation, collaboration, and so much more. Most of all,
                    they’re fun, and one could get even the most difficult of the students to engage! An <a
                        href="#">article </a>in the
                    claimed that games increase the levels of
                    dopamine in the midbrain section, which boost retention and learning. Games also increase overall
                    positivity inside a classroom. Researchers even suggest that teachers aim for at least one game,
                    short or otherwise, in their classes every day!</p>

                <p>If that’s not enough, think about the time students spend at school every day. In Nepal, students
                    spend about 7-8 hours in school every single day. That’s enough time for even adults to become
                    irritated by the mundane and the repetition. Games help add flare and excitement, making that
                    mundane desirable. Students would ultimately like going to school instead of dreading the thought
                    every morning. Imagine the positive attitude towards the day that would bring!</p>

                <p>
                    <img alt="" src="app/public/new/1.jpg">
                </p>

                <h2>Engagement and Motivation</h2>

                <p>If you are a teacher reading this, you probably have a student that you need to keep pushing in class
                    every day, begging them to engage. Or maybe you are a parent who keeps getting calls from your kids’
                    school because your kid refuses to participate. Perhaps you are a student who finds it difficult to
                    speak up in class because your classmates seem ‘too smart’ in comparison. Games could be a solution
                    to all that. Games also make students feel responsible for their own learning and development.
                    Nobody likes to lose, especially at games, which could be a source of motivation.</p>

                <p>Students may feel intimidated when a classroom is principally focused on learning and excellence.
                    There are students who may have a headstart in terms of what they already know, and that surely
                    could scare other students, making them believe they’re not ‘good enough!’ Games, however, help
                    level the arena. Most group-based games depend on collaboration as opposed to tests and exams that
                    entirely depend on individual skills. When students feel like their contribution is valued, they are
                    motivated to engage and participate. You know, a “hey, I’m good at this!” scenario.&nbsp;</p>

                <h2>Development of Skill Set&nbsp;</h2>

                <p>There are a lot of games that help students develop skill sets. <em>The Dictionary game</em>, for
                    instance, enables students to learn new words every time they play it. It especially matters because
                    in a game-like setting, every new information acquired tends to be retained well. As opposed to a
                    teacher asking students to remember a word, playing Dictionary is a much better way to ensure that
                    the new word is remembered.&nbsp;</p>

                <p>Moreover, think about <em>Snakes and Ladders</em>. Jack, a student, rolls a 5 while his piece is
                    rested on 11. He counts 12, 13, 14, 15, 16 and voila; he just completed simple addition all on his
                    own!&nbsp;</p>

                <p>How about Monopoly? Students work out how to manage funds, so they don’t end up getting bankrupt!
                    Additionally, there’s also a bit of luck involved which makes it all the more exciting. Moreover,
                    there’ll always be a teacher around to guide students through the concepts they acquire, which means
                    that learning will be efficient as opposed to when students play on their own. &nbsp;</p>

                <p>Let’s not forget social skills. Think of the game <em>I Messed Up</em>. Teachers ask students to
                    share experiences of something embarrassing they said or did. Maybe they used a word wrong? When
                    shared, these could prove to be a learning experience for others listening.&nbsp;</p>

                <p><img alt="" src="app/public/new/2.jpg">
                </p>

                <h2>Better Understanding of Key Concepts</h2>

                <p>Imagine a game of Let’s Pretend. Every student pretends to be a number. Let’s say Student A is 5, and
                    Student B is a 3. The teacher then asks Student A to stand beside a Student, which would make the
                    pair an 8. Student A walks down to Student B and says, “we’re eight”. Now imagine a different class
                    where a teacher hands out a worksheet that just says “5+3=?”. Which strategy do you think yields
                    better results?</p>

                <p>Let’s take this to high school students. Students try to personify different countries when being
                    taught about the World War. Student Germany makes everyone around them sit, while different
                    students, the UK and the USA, help others stand back up. There’ll be laughter, a little fun, and at
                    the end a group of well-learned pupils! Of course, these will play out in more detail than it sounds
                    here.&nbsp;</p>

                <p><img alt="" src="app/public/new/2.png">
                </p>

                <h2>Some Games Teachers Could Try in Class</h2>

                <p>Here are some classroom games for kids, as well as high schoolers.</p>

                <ul>
                    <li>
                        <p><strong>Pictionary</strong>: A perfect vocabulary booster since forever which never gets old.
                        </p>
                    </li>
                    <li>
                        <p><strong>Bingo</strong>: The classic. Could be used with almost any category. Synonyms,
                            guessing words from description, finding numbers in a row that add up to 10!</p>
                    </li>
                    <li>
                        <p><strong>Twenty Objects</strong>: The teacher places 20 objects on the table, lets a group of
                            students see them, then covers them with a piece of cloth. Students then work out what
                            objects they saw, in order, as a group, thus boosting memory as well as trust and
                            collaboration.&nbsp;</p>
                    </li>
                    <li>
                        <p><strong>Dictionary</strong>: Teachers pick a random obscure word from the dictionary, write 3
                            different meanings, one of which is correct. Then teachers ask students to guess the correct
                            meaning out of the options. It encourages students to make mistakes which is probably the
                            most efficient learning strategy.&nbsp;</p>
                    </li>
                    <li>
                        <p><strong>Taboo</strong>: Teachers assign students a random word. The student then tries to
                            help their classmates guess the correct word without using relatable words. For instance, if
                            the word was “ice”, the students cannot use “cold”, “water”, “mountains”, etc. It is perfect
                            for vocabulary as well as understanding key concepts.&nbsp;</p>
                    </li>
                </ul>

                <h2>&nbsp;A Different Route to Learning</h2>

                <p>Games provide an alternative route towards learning. Evolution is a prerequisite to development, and
                    with everything going on, classrooms should and deserve to evolve too. However, intervention from
                    teachers is an absolute necessity. Teachers should be prepped to analyze and evaluate the results
                    the games bring. After all, like everything else, it only gets better after a series of trials and
                    errors. &nbsp;Teachers need to identify which games work best and what games they think will assist
                    in the matter. Are the games accessible to all? Is every student enthusiastic about it? Everything
                    needs to be evaluated for this change to align with its best variation. &nbsp;<br>
                    &nbsp;</p>
